# طلب الرقم من المستخدم
num = int(input("الرجاء إدخال رقم أكبر من صفر: "))

# التأكد من صحة الرقم
if num <= 0:
    num = int(input("الرجاء إدخال رقم أكبر من صفر: "))

# حساب المجموع
sum = 0
for i in range(1, num+1):
    sum += i

# عرض النتيجة
print("مجموع الأرقام من 1 إلى ", num, " هو: ", sum)
